# Arithmetic Operators Example 

# Define two numbers
a = 5
b = 3

# Perform arithmetic operations
sum_ = a + b           # Addition
difference = a - b     # Subtraction
product = a * b        # Multiplication
quotient = a / b       # Division (float result)
floor_div = a // b     # Floor division (whole number result)
remainder = a % b      # Modulus (remainder)
power = a ** b         # Exponentiation (a to the power of b)

# Display the results
print("a =", a)
print("b =", b)
print("----------------------------")
print("Sum:", sum_)
print("Difference:", difference)
print("Product:", product)
print("Quotient:", quotient)
print("Floor Division:", floor_div)
print("Remainder (Modulus):", remainder)
print("Power (a ** b):", power)

